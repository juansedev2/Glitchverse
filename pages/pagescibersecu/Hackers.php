            <div class="texto2-1">
              <p>El termino "hacker" tiene definiciones diversas en el sentido de su definicion litereal; pero en general, se puede definir como una persona que tiene conocimientos avanzados de programación y manipulación de sistemas informáticos en general, capaz de realizar acciones que no pueden hacer usuarios comunes y que para ello demanda un conocimiento concreto del sistema en el que tratre.</p>
              <p>Los hackers están capacitados para poder acceder a los sistemas informáticos (entiendase sistema informático como una terminal, servidor, la web, bases de datos, software, etc.), con el objetivo de comprender su funcionalidad en el aspecto de la seguridad. Esto lleva a que pueda manipular de cierta forma el sistema en el que está y descubrir problemas y riesgos que estos presenten, para poder acceder a lo "profundo" del sistema y ver una posible modificación si fuese posible; pero claro, el hacker no está en función de destruir, al contrario, es de ayudar a solucionar esos problemas de seguridad, bien sea haciendolo el mismo o informando al dueño del sistema. Esto se hace en pro de la protección de la información, ya que este es un bien, que a pesar de ser intangible, su valor es muy grande.</p>
              <p>En la actualidad, a pesar de tener conocimiento sobre lo que es un hacker, siempre se da la misma concepcion común de este en la sociedad, como por ejemplo lo que habíamos mencionado anteriormente, que es una persona mala y que destruye sistemas, eso no es cierto y esto es debido a que no se tiene el conocimiento de que quienes hacen eso son los <span> "crackers"</span>, que a diferencia de los <span style="color:green;">hackers</span>, estos si utilizan su conocimiento para hacer daño en los sistemas.</p>
