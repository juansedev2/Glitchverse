              <p>Las cookies y su consumo se vienen produciendo por temas de comodidad, seguridad y hasta intereses comerciales. Tenemos el ejemplo de tener una sesión en una aplicación web por ejemplo, allí las cookies están presentes para que podamos tener una accesibilidad adecuada y rápida, carga previa de contenido y demás funciones. Esto puede brindar algo agradable al usuario, ayudando en la gestión de sus cuentas. Otro caso son las compras en línea. Hay empresas que ofrecen compras en línea y tienen una página web capaz de ofrecer las funciones necesarias para un adecuada compra, esto con énfasis de ofrecerle facilidad y comodidad al usuario. ¿Alguna vez has comprado en linea o has estado en un sitio web de ese estilo?, ¿has visto un ícono de carrtiro de compras?, eso es una cookie por ejemplo, que en ese caso, tiene la función de almacenar los objetos para compra y su manipulación (agregar, quitar, consultar). Con esto, ofrece una experiencia intersante.</p>
