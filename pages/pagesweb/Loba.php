        <h1> PÁGINA LOBA </h1>
        <h2> ¿Qué es una página loba? </h2>
        <p> Las páginas lobas son páginas que en palabras de un desarrollador experto son: "Páginas chambonas", y por "chambón" me refiero a que es mal hecho o tiene mal imagen, sin sentido u otras cosas que se podrían también decir; pero en realidad vienen no vienen siendo algo malo tampoco, es más, las páginas lobas son producto de una persona que inicia por primera vez en experimentar HTML junto con CSS.</p>
        <p> Cuando se inician a experimentar esas tecnologías, es común cometer errores, cosas raras, irracionales, descuadradas, entre otras; pero no decimos esto con ánimo de ofender, ya que de este tipo de cosas se puede aprender mucho y es más, pueden producirse páginas lobas muy interesantes y TAN LOBAS, que son buenas en realidad. Consideramos que hacer una página loba es algo casi que importante cuando se comienza en el desarrollo web, ya que es una actividad chévere para ir aprendiendo y experimentando las tecnologías mencionadas (HTML, CSS).</p>
        <p>Tú puedes desarrollar tú pagina loba a tu manera, quizá esa sea la característica más importante que tiene este tipo de páginas, ya que nos da la libertad de desarrollar lo que nos gusta. Si estas iniciando en desarrollo web o eres aficionado, te invitamos a crear tu página web, para vayas aprendiendo del manejo de esas herramientas de desarrollo web. Recuerda que el objetivo a la larga, es aprender, y porque nó, pasarla bien practicando.</p>
        <p> A continuación, te presentaremos unos ejemplos de páginas lobas, hechas por desarrolladores que también lo hicieron en sus inicios con las tecnologías, cada una tiene cosas especiales, así que te invitamos a que no solo las mires, sino las analices también, y quizá, encuentres secretos. Además consideramos que no es necesario una descripción, quizá eso le da un toque más de intriga y juicio por parte del espectador.</p>
