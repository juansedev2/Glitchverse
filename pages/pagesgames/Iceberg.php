          <p> En los videojuegos, los iceberg se limitan unicamente a tratar únicamente en ese campo que está tratando, es decir, trata de no involucrar cosas externas totalmente ajenas al tema, a no ser de que estas posean una estrecha relación, en ese caso no serían totalmente ajenas. Comentamos esto debido a que es usual que al navegar un iceberg, se encuentren cosas que se suponen no deberían estar relacionadas con el tema principal, para eso debe hacerse una justificación del porqué se encuentra ahí. Ahora bien, en cuanto a la representación, para entenderla rapidamente, haremos un resumen de la metodología de este.</p>
          <p>El iceberg se divide en partes, desde su superficie hasta su profundidad, a su vez, puede estar particionado en niveles, esto último ya es arbitrario por la cantidad de información que se encuentre disponible en ese iceberg y quien lo diseña; pero en general, la analogía es la misma, es decir, lo que está en la superficie es lo más conocido en general, y lo que está en más profundidad es que es poco conocido y así mientas más profundo sea, es porque todavía es más desconocido y sercreto por enede. Ahora bien, ¿donde podemos encontrar iceberg de los videojuegos?</p>
