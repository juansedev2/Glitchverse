          <h1 id="ConceptoIceberg">¿Que es el concepto de Iceberg?</h1>
          <div class="linea"></div>
          <p>El concepto de iceberg proviene de la <span>analogía</span> de este, es decir, recordando que analogía significa la relación de semejanza entre cosas distintas, se aplica en este caso, en la relación simbólica de un iceberg literal, a un razonamiento visual por parte del ser humano. Si pensamos en un iceberg, imaginamos un gran trozo de hielo en el océano, mayoritariamente recordamos solo la superfice más <span> nunca su interior</span>. Resulta ser algo típico este tipo de visión, ya que no solemos recordar lo que nunca vemos, por ende, vemos tan solo una parte de lo que en realidad es un iceberg.</p>
          <p>Esta misma analogía, la podemos asociar con diversos campos humanos, generalidades y temas específicos, donde lo que se hace es "profundizar en el tema a tratar". Esto se hace con investigación, lo cual puede llevar a descubrir cosas que no están a la luz, de estarlo, son conocidas escasamente. Esto logra llamar la atención por el conocimiento, el poder conocer cosas que antes no sabíamos. En esa investigación, se pueden extraer diversos resultados, desde hechos concretos hasta teorías formales, conspiraciones, debates,etc.</p>
          <p>Resulta curioso el pensar detenidamente donde podemos aplicar esta analogía, ya que nos abre a un nivel de <span>conocimiento profundo</span>. Ahora, buscar un iceberg y entenderlo al 100% no es facil, ya que es algo que escasamente se suele hacer por investigación. Decimos esto porque realmente buscar iceberg de distintos temas, puede llevarnos tanto a búsquedas exitosas como fallidas. No toda información está disponible facilmente, por tanto, se debe ser conciente de esto a la hora de investigar sobre cualquier tema y hacer un iceberg de este. Pese a que ya existen muchos, faltan un montón por descubrir, esto basado en todos los campos del saber humano existentes posibles.</p>
          <p>Eso a grosso modo, es un iceberg. Si estas interesado en que hablemos más de este tema, por favor contacta con nosotros y dependiendo de que tanto sea exigido, entonces crearemos un artículo dedicado especialmente a este tema.</p>
